package com.example.csit228_f1_v2;

public class User {
    public int uid;
    public String name;
    public String email;
    public String username;
    public String password;

    public User(int uid, String name, String email, String username, String password) {
        this.uid = uid;
        this.name = name;
        this.email = email;
        this.username = username;
        this.password = password;
    }
}
